# Type of the value
x = 7
print('x is {}'.format(x))
print(type(x))

#  String Type

x = 'seven'.upper()
print('x is {}'.format(x))
print(type(x))

## F -strings
a = 8
b = 9

x = f'seven {a} {b}'
print('x is {}'.format(x))
print(type(x))

### OR

x = 'seven {} {}'.format(8,9)
print('x is {}'.format(x))
print(type(x))

# Numeric Type
x = 7
print('x is {}'.format(x))
print(type(x))

y = 7 % 3 # Division 
print(y)

z = 7 // 3 # Reminder
print(z)